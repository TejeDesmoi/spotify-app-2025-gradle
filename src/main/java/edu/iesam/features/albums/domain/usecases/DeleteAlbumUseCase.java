package edu.iesam.features.albums.domain.usecases;

import edu.iesam.features.albums.domain.repository.AlbumRepository;

public class DeleteAlbumUseCase {
    private AlbumRepository albumRepository;

    public DeleteAlbumUseCase(AlbumRepository albumRepository){this.albumRepository = albumRepository;}

    public void execute(String id){
        albumRepository.deleteAlbum(id);
    }

}
